#' @title
#' get /vendor/orders/\{orderId\}/results
#'
#' @description
#' Get the results of a specific Order
#'
#' @param con list; required: TRUE; BrAPI connection object
#' @param orderId character; required: TRUE; The order identifier returned by the
#'    vendor, when the order was successfully submitted with **POST**
#'    **/vendor/orders** as implemented in the `brapi_post_orders()` function.
#' @param page integer; required: FALSE; Used to request a specific page of data
#'    to be returned. The page indexing starts at 0 (the first page is
#'    `page = 0`). Default is `0`.
#' @param pageSize integer; required: FALSE; The size of the pages to be
#'    returned. Default is `1000`.
#'
#' @details Retrieve the data files generated by the vendors analysis
#'
#' @return data.frame
#'
#' @author Maikel Verouden
#'
#' @references \href{https://app.swaggerhub.com/apis/PlantBreedingAPI/BrAPI-Genotyping/2.0#/Vendor/get_vendor_orders__orderId__results }{BrAPI SwaggerHub}
#'
#' @family brapi-genotyping
#' @family Vendor
#'
#' @examples
#' \dontrun{
#' con <- brapi_db()$testserver
#' out <- brapi_get_vendor_orders_orderId_status(con = con,
#'                                        orderId = "vendor_order1")
#' while (out[["status"]] != "completed") {
#'  out <- brapi_get_vendor_orders_orderId_status(con = con,
#'                                                orderId = "vendor_order1")}
#' if (out[["status"]] == "completed") {
#'  brapi_get_vendor_orders_orderId_results(con = con,
#'                                          orderId = "vendor_order1")}
#' }
#'
#' @export
brapi_get_vendor_orders_orderId_results <- function(con = NULL,
                                                    orderId = '',
                                                    page = 0,
                                                    pageSize = 1000) {
  ## Create a list of used arguments
  usedArgs <- brapirv2:::brapi_usedArgs(origValues = FALSE)
  ## Check if BrAPI server can be reached given the connection details
  brapi_checkCon(con = usedArgs[["con"]], verbose = FALSE)
  ## Check validity of used and required arguments
  brapirv2:::brapi_checkArgs(usedArgs, reqArgs = "orderId")
  ## Obtain the call url
  callurl <- brapirv2:::brapi_GET_callURL(usedArgs = usedArgs,
                                          callPath = "/vendor/orders/{orderId}/results",
                                          reqArgs = "orderId",
                                          packageName = "BrAPI-Genotyping",
                                          callVersion = 2.0)

  try({
    ## Make the call and receive the response
    resp <- brapirv2:::brapi_GET(url = callurl, usedArgs = usedArgs)
    ## Extract the content from the response object in human readable form
    cont <- httr::content(x = resp, as = "text", encoding = "UTF-8")
    ## Convert the content object into a data.frame
    out <- brapirv2:::brapi_result2df(cont, usedArgs)
  })
  ## Set class of output
  class(out) <- c(class(out), "brapi_get_vendor_orders_orderId_results")
  ## Show pagination information from metadata
  brapirv2:::brapi_serverinfo_metadata(cont)
  return(out)
}
